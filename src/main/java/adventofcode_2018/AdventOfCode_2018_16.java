	private static Register executeInstruction(Register start, Instruction instruction, String name) {
		Register result = new Register(start.register0, start.register1, start.register2, start.register3, AdventOfCodeUtil.cloneList(start.direct));
		if (name.equals("addr") && !seenCodes.contains("addr")) {
			lastCode = "addr";
			result.direct.set(instruction.C, start.direct.get(instruction.A) + start.direct.get(instruction.B));
		}

		if (name.equals("addi") && !seenCodes.contains("addi")) {
			lastCode = "addi";
			result.direct.set(instruction.C, start.direct.get(instruction.A) + instruction.B);
		}
		if (name.equals("mulr") && !seenCodes.contains("mulr")) {
			lastCode = "mulr";
			result.direct.set(instruction.C, start.direct.get(instruction.A) * start.direct.get(instruction.B));
		}
		if (name.equals("muli") && !seenCodes.contains("muli")) {
			lastCode = "muli";
			result.direct.set(instruction.C, start.direct.get(instruction.A) * instruction.B);
		}

		if (name.equals("banr") && !seenCodes.contains("banr")) {
			lastCode = "banr";
			result.direct.set(instruction.C, start.direct.get(instruction.A) & start.direct.get(instruction.B));
		}

		if (name.equals("bani") && !seenCodes.contains("bani")) {
			lastCode = "bani";
			result.direct.set(instruction.C, start.direct.get(instruction.A) & instruction.B);
		}

		if (name.equals("borr") && !seenCodes.contains("borr")) {
			lastCode = "borr";
			result.direct.set(instruction.C, start.direct.get(instruction.A) | start.direct.get(instruction.B));
		}

		if (name.equals("bori") && !seenCodes.contains("bori")) {
			lastCode = "bori";
			result.direct.set(instruction.C, start.direct.get(instruction.A) | instruction.B);
		}

		if (name.equals("setr") && !seenCodes.contains("setr")) {
			lastCode = "setr";
			result.direct.set(instruction.C, start.direct.get(instruction.A));
		}
		if (name.equals("seti") && !seenCodes.contains("seti")) {
			lastCode = "seti";
			result.direct.set(instruction.C, instruction.A);
		}

		if (name.equals("gtir") && !seenCodes.contains("gtir")) {
			lastCode = "gtir";
			if (start.direct.get(instruction.B) >= instruction.A) {
				result.direct.set(instruction.C, 0);
			} else {
				result.direct.set(instruction.C, 1);
			}
		}

		if (name.equals("gtri") && !seenCodes.contains("gtri")) {
			lastCode = "gtri";
			if (start.direct.get(instruction.A) > instruction.B) {
				result.direct.set(instruction.C, 1);
			} else {
				result.direct.set(instruction.C, 0);
			}
		}

		if (name.equals("gtrr") && !seenCodes.contains("gtrr")) {
			lastCode = "gtrr";
			if (start.direct.get(instruction.A) > start.direct.get(instruction.B)) {
				result.direct.set(instruction.C, 1);
			} else {
				result.direct.set(instruction.C, 0);
			}
		}


		if (name.equals("eqir") && !seenCodes.contains("eqir")) {
			lastCode = "eqir";
			if (start.direct.get(instruction.B) == instruction.A) {
				result.direct.set(instruction.C, 1);
			} else {
				result.direct.set(instruction.C, 0);
			}
		}

		if (name.equals("eqri") && !seenCodes.contains("eqri")) {
			lastCode = "eqri";
			if (start.direct.get(instruction.A) == instruction.B) {
				result.direct.set(instruction.C, 1);
			} else {
				result.direct.set(instruction.C, 0);
			}
		}

		if (name.equals("eqrr") && !seenCodes.contains("eqrr")) {
			lastCode = "eqrr";
			if (start.direct.get(instruction.A).equals(start.direct.get(instruction.B))) {
				result.direct.set(instruction.C, 1);
			} else {
				result.direct.set(instruction.C, 0);
			}
		}
		return result;
	}

